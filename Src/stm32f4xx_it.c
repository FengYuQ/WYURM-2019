/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */
	uint32_t  tmp_flag = 0;
	uint32_t  temp = 0;
  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart3);
  /* USER CODE BEGIN USART3_IRQn 1 */
tmp_flag = __HAL_UART_GET_FLAG(&huart3,UART_FLAG_IDLE);
	
	if((tmp_flag != RESET)){
		
		__HAL_UART_CLEAR_IDLEFLAG(&huart3);
		
		/* 读取串口状态寄存器（芯片型号不同，寄存器名称可能需要修改） */
		temp = huart3.Instance->SR;
		/* 读取串口数据寄存器（芯片型号不同，寄存器名称可能需要修改） */
		temp = huart3.Instance->DR;
		HAL_UART_DMAStop(&huart3);
		/* 读取DMA剩余传输数量（芯片型号不同，寄存器名称可能需要修改） */
		temp = hdma_usart3_rx.Instance->NDTR;
		
		if(temp == BUFFER_SIZE - 23 - 24 - 25){
			if(rx_buffer[53] == 0x01 && rx_buffer[54] == 0x02 && Verify_CRC16_Check_Sum(&rx_buffer[48],24))
			{
				//剩余血量
				FT.U[1] = rx_buffer[58];
				FT.U[0] = rx_buffer[57];
				SkyGuard_state.remain_HP = FT.I;
				
				if(SkyGuard_state.remain_HP <= 100)
				{
					Flag.Mode = 0;
					Flag.PatrolFlag = 1;
					Flag.ResidueFlag = 1;
				}

//				//满血血量
//				FT.U[1] = rx_buffer[60];
//				FT.U[0] = rx_buffer[59];
//			
//				SkyGuard_state.max_HP = FT.I;
//				
//				printf("满血血量=%d\r\n",SkyGuard_state.max_HP);
			
			}
		}
		rx_len = BUFFER_SIZE - temp;
		recv_end_flag = 1;
	}
  /* USER CODE END USART3_IRQn 1 */
}

